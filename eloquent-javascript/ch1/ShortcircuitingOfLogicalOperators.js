let name = "anshul"
let otherName ="anshul"
let blankStr ="";

/*  And operator example..when the value to its left is 
 something that converts to false,it returns that value
  and otherwise it returns value to its right */

console.log(name && otherName);
console.log(name && blankStr);


/*  OR operator example..when the value to its left is 
 something that converts to true,it returns that value
 and otherwise it returns value to its right */

console.log(name || otherName);

